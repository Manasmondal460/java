// For loop example
public class LoopsExample {

    public static void main(String[] args) {
        // Step 2: Example code for the `for` loop
        System.out.println("For Loop:");
        for (int i = 1; i <= 5; i++) {
            System.out.print(i + " ");
        }
        System.out.println();
    }
}
